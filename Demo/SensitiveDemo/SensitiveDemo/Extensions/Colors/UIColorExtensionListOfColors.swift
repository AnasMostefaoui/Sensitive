import Foundation
import UIKit

extension UIColor {
    
    class var Sensitive_greenColor: UIColor {
        get {
            return UIColor(red: 87.0 / 255.0, green: 225.0 / 255.0, blue: 160.0 / 255.0, alpha: 255.0 / 255.0)
        }
    }
    
}